package com.neuedu.planewar.core;

/**
 * 八(九)个方向的枚举类
 * @author 皇up
 *
 */
public enum Direction {
	/**
	 * 左
	 */
	LEFT,
	/**
	 * 左上
	 */
	LEFT_UP,
	/**
	 * 上
	 */
	UP,
	/**
	 * 右上
	 */
	RIGHT_UP,
	/**
	 * 右
	 */
	RIGHT,
	/**
	 * 右下
	 */
	RIGHT_DOWN,
	/**
	 * 下
	 */
	DOWN,
	/**
	 * 左下
	 */
	LEFT_DOWN,
	/**
	 * 静止
	 */
	STOP
}
